package com.luying.bean;

import org.springframework.beans.factory.annotation.Value;

/**
 * @description:
 * @author: lijian
 * @create: 2018-08-14
 **/
public class Person {
    @Value("${person.name}")
    private String name;
    private int age;

    public Person() {
    }

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public String toString() {
        return "Person{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
